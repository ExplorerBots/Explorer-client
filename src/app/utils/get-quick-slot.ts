export const getQuickSlot = (slot: number) => {
   switch (slot) {
      case 36:
         return 0;
      case 37:
         return 1;
      case 38:
         return 2;
      case 39:
         return 3;
      case 40:
         return 4;
      case 41:
         return 5;
      case 42:
         return 6;
      case 43:
         return 7;
      case 44:
         return 8;
      default:
         return 0;
   }
};
